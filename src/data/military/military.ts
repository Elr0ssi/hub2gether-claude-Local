export function getMilitaryData(year: number): Record<string, MilitaryCountryData> {
  const years = Object.keys(DATA).map(Number).sort((a, b) => a - b);
  const nearest = years.reduce((prev, curr) =>
    Math.abs(curr - year) < Math.abs(prev - year) ? curr : prev
  );
  return DATA[nearest] ?? {};
}

function interpolateColor(hex1: string, hex2: string, t: number): string {
  const r1 = parseInt(hex1.slice(1, 3), 16), g1 = parseInt(hex1.slice(3, 5), 16), b1 = parseInt(hex1.slice(5, 7), 16);
  const r2 = parseInt(hex2.slice(1, 3), 16), g2 = parseInt(hex2.slice(3, 5), 16), b2 = parseInt(hex2.slice(5, 7), 16);
  const r = Math.round(r1 + (r2 - r1) * t);
  const g = Math.round(g1 + (g2 - g1) * t);
  const b = Math.round(b1 + (b2 - b1) * t);
  return `#${r.toString(16).padStart(2,"0")}${g.toString(16).padStart(2,"0")}${b.toString(16).padStart(2,"0")}`;
}

const METRIC_COLORS: Record<MilitaryMetricId, [string, string]> = {
  budget:   ["#BBDEFB", "#0D47A1"],
  soldiers: ["#DCEDC8", "#1B5E20"],
  tanks:    ["#EFEBE9", "#3E2723"],
  ships:    ["#E0F7FA", "#004D40"],
  aircraft: ["#FFF8E1", "#BF360C"],
};

export function getColorForMilitary(
  countryName: string,
  metric: MilitaryMetricId,
  data: Record<string, MilitaryCountryData>
): string {
  const countryData = data[countryName];
  if (!countryData) return "#EBEBEB";
  const value = countryData[metric];
  if (!value) return "#EBEBEB";

  const values = Object.values(data).map(d => d[metric]).filter(v => v > 0);
  const max = Math.max(...values);
  const ratio = Math.pow(value / max, 0.38);
  return interpolateColor(METRIC_COLORS[metric][0], METRIC_COLORS[metric][1], ratio);
}
